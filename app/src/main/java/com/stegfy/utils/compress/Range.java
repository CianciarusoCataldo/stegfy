package com.stegfy.utils.compress;

class Range {

    private Double lower;
    private Double higher;

    Double getHigher() {
        return higher;
    }

    Double getLower() {
        return lower;
    }

    void setLower(Double lower) {
        this.lower = lower;
    }

    void setHigher(Double higher) {
        this.higher = higher;
    }
}
